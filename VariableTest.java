class VariableTest{
       public static void main(String args[])
       {
                   byte a=1;             //smallest memory unit in java is bit
                   short b=10;
                   int c=20;
                   long d=1004;
                   boolean e=true;
                   float f=10.2f;
                   double g=10.23;
                   char ch='A';
       
                   System.out.println("Value of bit:" + a);
                   System.out.println("Value of short:" + b);
                   System.out.println("Value of int:" + c);

                   System.out.println("Value of long:" + d);
                   System.out.println("Value of boolean:" + e);
                   System.out.println("Value of float:" + f);
                   System.out.println("Value of double:" + g);
                   System.out.println("Value of ch:" + ch);
       }
}