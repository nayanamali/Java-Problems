class  relational
{

      public static void main(String args[])
   {
      
       int no1=10;
       int no2=20;
       System.out.println();
       System.out.println("Relational Operators");
       System.out.println("no1 > no2:" + (no1>no2));
       System.out.println("no1 < no2:" + (no1<no2));
       System.out.println("no1 >= no2:" + (no1>=no2));
       System.out.println("no1 <= no2:" + (no1<=no2));
       System.out.println("no1 == no2:" + (no1==no2));
       System.out.println("no1 != no2:" + (no1!=no2));
   }
}

