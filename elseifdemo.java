class elseifdemo{
    
               public static void main(String args[])
               {
                         int num=-5;
                         if(num>0)
                         { 
                               System.out.println("Given number is positive");
                         }
                         else if(num<0)
                        {
                             System.out.println("Given number is negative");
                        }
                        else
                        {
                            System.out.println("Given number is zero");
                        }
               }
}