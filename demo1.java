class demo1{
   
     public static void main(String args[])
     {
              int count=0;
              while(count<5)
              {
                       count++;
              }
              System.out.println("Using While loop");
              System.out.println(count);
            
              do
              {
                      count++;
 
              }while(count<5);
              System.out.println(count);
     }
}
